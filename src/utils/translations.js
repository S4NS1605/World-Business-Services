function translateOffline(text) {
  let translated = text;
  for (const [regex, replacement] of commonReplacements) {
    translated = translated.replace(regex, replacement);
  }
  return translated;
}

/**
 * Translates text from Spanish to English synchronously using local dictionary,
 * replacements list, and memory cache. Avoids async lifecycle issues.
 * @param {string} text 
 * @returns {string}
 */
export function translateEsToEnSync(text) {
  if (!text || typeof text !== 'string') return text;
  const trimmed = text.trim();
  if (!trimmed) return text;

  // 1. Check cache first
  if (translationCache[trimmed]) {
    return translationCache[trimmed];
  }

  // 2. Check dictionary
  if (medicalDictionary[trimmed]) {
    return medicalDictionary[trimmed];
  }

  // 3. Fallback to common technical regex replacements
  return translateOffline(trimmed);
}

// Cache to store translated descriptions to avoid hitting API rate limits repeatedly. Loaded from localStorage for 0ms load speed.
const translationCache = (() => {
  try {
    const saved = localStorage.getItem('wbs_translation_cache');
    if (!saved) return {};
    const parsed = JSON.parse(saved);
    
    // Auto-heal cache: delete any legacy warning values/keys to clear corrupt displays instantly
    let cacheChanged = false;
    Object.keys(parsed).forEach(key => {
      const val = String(parsed[key]).toUpperCase();
      const keyUpper = String(key).toUpperCase();
      if (
        val.includes('MYMEMORY WARNING') || 
        val.includes('LIMIT EXCEEDED') || 
        val.includes('IS NOT A VALID') ||
        val.includes('TOO MANY REQUESTS') ||
        keyUpper.includes('MYMEMORY WARNING') ||
        keyUpper.includes('LIMIT EXCEEDED')
      ) {
        delete parsed[key];
        cacheChanged = true;
      }
    });
    
    if (cacheChanged) {
      localStorage.setItem('wbs_translation_cache', JSON.stringify(parsed));
    }
    return parsed;
  } catch (e) {
    return {};
  }
})();
let isTranslationApiBlocked = false;

// Helper to save translations to localStorage cache
function saveCache() {
  try {
    localStorage.setItem('wbs_translation_cache', JSON.stringify(translationCache));
  } catch (e) {
    console.warn('Failed to save translation cache to localStorage:', e);
  }
}

/**
 * Automatically translates a given text from Spanish to English using MyMemory API
 * @param {string} text - The source text in Spanish
 * @returns {Promise<string>} The translated text in English (or original if failed)
 */
export async function translateEsToEn(text) {
  if (!text || typeof text !== 'string') return text;
  
  const trimmed = text.trim();
  if (!trimmed) return text;

  // Check cache first
  if (translationCache[trimmed]) {
    return translationCache[trimmed];
  }

  // Check dictionary for quick perfect translations
  if (medicalDictionary[trimmed]) {
    return medicalDictionary[trimmed];
  }

  // If translation API was blocked in this session, fallback to offline or Spanish (never show warnings)
  if (isTranslationApiBlocked) {
    const offline = translateOffline(trimmed);
    return offline === trimmed ? trimmed : offline;
  }

  // If text is short (under 25 chars), translate offline using replacements
  if (trimmed.length < 25) {
    const offlineTrans = translateOffline(trimmed);
    if (offlineTrans !== trimmed) {
      translationCache[trimmed] = offlineTrans;
      saveCache();
      return offlineTrans;
    }
  }

  try {
    const res = await fetch(`https://translate.googleapis.com/translate_a/single?client=gtx&sl=es&tl=en&dt=t&q=${encodeURIComponent(trimmed)}`);
    if (!res.ok) throw new Error(`Google Translate responded with HTTP ${res.status}`);

    const data = await res.json();
    if (data && data[0]) {
      const translated = data[0].map(x => x[0]).filter(Boolean).join('');
      if (translated && translated.trim()) {
        // Fail-safe: verify we don't accidentally save warnings (Google doesn't return these warnings, but good practice)
        if (
          translated.toUpperCase().includes('MYMEMORY WARNING') || 
          translated.toUpperCase().includes('LIMIT EXCEEDED')
        ) {
          const offline = translateOffline(trimmed);
          return offline === trimmed ? trimmed : offline;
        }
        translationCache[trimmed] = translated; // save to cache
        saveCache();
        return translated;
      }
    }
  } catch (err) {
    console.warn('Google translation failed, returning original/offline:', err);
  }

  const offline = translateOffline(trimmed);
  return offline === trimmed ? trimmed : offline;
}

/**
 * Translates an entire product object (name, description, specs) into English
 * @param {object} product - The product object from Supabase (in Spanish)
 * @returns {Promise<object>} A new product object translated to English
 */
export async function translateProductToEn(product) {
  if (!product) return product;

  try {
    const hasDescEn = product.description_en && product.description_en.trim();
    const hasSpecsEn = product.specs_en && Array.isArray(product.specs_en) && product.specs_en.length > 0;

    if (hasDescEn && hasSpecsEn) {
      return {
        ...product,
        description: product.description_en,
        specs: product.specs_en
      };
    }

    const [translatedDesc, translatedSpecs] = await Promise.all([
      hasDescEn ? product.description_en : translateEsToEn(product.description),
      hasSpecsEn ? product.specs_en : Promise.all((product.specs || []).map(spec => translateEsToEn(spec)))
    ]);

    return {
      ...product,
      name: product.name, // Keep original name! DO NOT translate model names/proper nouns (avoids limits)
      brand: product.brand, // Brands do not require translation
      category: product.category, // Category remains as ID key
      description: translatedDesc,
      specs: translatedSpecs
    };
  } catch (e) {
    console.error('Error translating product:', e);
    return product;
  }
}
